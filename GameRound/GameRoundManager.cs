namespace LCObjectiveMod.GameRound
{
    internal class GameRoundManager
    {
        //-----------------------------------------------------------------------------------------
        //                                   PUBLIC
        //-----------------------------------------------------------------------------------------

        //-----------------------------------------------------------------------------------------
        public void Reset()
        {
            _bDoorHasBeenOpened = false;
            _bPlayerHasBeenCrouch = false;
            _bPlayerHasJump = false;
        }

        //-----------------------------------------------------------------------------------------
        //                                   Getters
        //-----------------------------------------------------------------------------------------

        //-----------------------------------------------------------------------------------------
        public bool GetDoorHasBeenOpened() => _bDoorHasBeenOpened;   
        
        //-----------------------------------------------------------------------------------------
        public bool GetPlayerHasBeenCrouch() => _bPlayerHasBeenCrouch;
                
        //-----------------------------------------------------------------------------------------
        public bool GetPlayerHasJump() => _bPlayerHasJump;

        //-----------------------------------------------------------------------------------------
        //                                   Setters
        //-----------------------------------------------------------------------------------------

        //-----------------------------------------------------------------------------------------
        public void SetDoorHasBeenOpened(bool ibValue)
        {
            _bDoorHasBeenOpened = ibValue;
        }  
        
        //-----------------------------------------------------------------------------------------
        public void SetPlayerHasBeenCrouch(bool ibValue)
        {
            _bPlayerHasBeenCrouch = ibValue;
        } 
        
        //-----------------------------------------------------------------------------------------
        public void SetPlayerHasJump(bool ibValue)
        {
            _bPlayerHasJump = ibValue;
        }

        //-----------------------------------------------------------------------------------------
        //                               Private members
        //-----------------------------------------------------------------------------------------

        private bool _bDoorHasBeenOpened;

        private bool _bPlayerHasBeenCrouch;

        private bool _bPlayerHasJump;
    }
}
